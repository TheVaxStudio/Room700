                    screen.blit(
                        self.resize_walls, 
                        camera.apply(
                            pygame.Rect(x * self.tile_size, y * self.tile_size, self.tile_size, self.tile_size)
                        ), 
                        special_flags = pygame.BLEND_RGBA_MULT
                    )
